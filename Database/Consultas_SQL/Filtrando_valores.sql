-- Seleciona valores diferentes.
SELECT * FROM products WHERE price <> 50;

-- Seleciona valores maior.
SELECT * FROM products WHERE price > 50;

-- Seleciona valores menor.
SELECT * FROM products WHERE price < 50;

-- Seleciona valores maiores ou iguais.
SELECT * FROM products WHERE price >= 50;

-- Seleciona valores menores ou iguais.
SELECT * FROM products WHERE price <= 50;