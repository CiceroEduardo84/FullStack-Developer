let number = 10;

// Incremento
// number = number + 1;
number++;

// INcrementa após(não mostra no console)
console.log(number++);
console.log(number);

// Incremento antes
console.log(++number);

// Decremento
// console.log("Decremento após:", number--);
// console.log(number--);
console.log("Decremento antes:", --number);

// Incrementar mais de 1(colocar)
number += 10;
console.log(number);

// Decrementar mais de 1(remover)
number -= 10;
console.log(number);
